#include <stdio.h>

int main() {
    int x, y, n = 0;
    scanf("%d%d", &x, &y);
    if (x == y) n = 100;
    else if (x % 10 == y / 10 && x / 10 == y % 10) n = 20;
    else if (x % 10 == y / 10 || x / 10 == y % 10 || x / 10 == y / 10 || x % 10 == y % 10) n = 2;
    printf("%d\n", n);
    return 0;
}

