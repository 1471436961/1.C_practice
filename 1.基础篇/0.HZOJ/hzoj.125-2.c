#include <stdio.h>

int main() {
    int h, m, s, t;
    scanf("%d%d%d%d", &h, &m, &s, &t);
    int t1 = 3600 * h + 60 * m + s + t;
    if (t1 >= 86400) {
        t1 -= 86400;
    }
    if (t1 < 43200) {
        if (t1 < 3600) {
            t1 += 43200;
        }
        printf("%d:%d:%d%s\n", t1 / 3600, t1 % 3600 / 60, t1 % 60, "am");
    }else {
        t1 -=43200;
        if (t1 < 3600) {
            t1 += 43200;
        }
        printf("%d:%d:%d%s\n", t1 / 3600, t1 % 3600 / 60, t1 % 60, "pm");
    }
    printf("%.2f%%\n", t / 86400.0 * 100);
    return 0;
}

